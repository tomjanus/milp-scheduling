function y = plot_scheduling_2p1t_results(init_sim_output, optim_output, ...
        optim_sim_output, input, sim, vars, folder)
    % Plot the results of the optimal pump scheduling exercise on a two 
    % pump one tank system.
    % Args:
    % init_sim_output - output from the simulation with initial schedules
    % optim_output - output from the MILP model
    % optim_sim_output - output from the simulation with optimized schedules
    % input - input structure (shared between all models)
    % sim - sim structure with simulation configuration parameters
    % vars - variables structure
    % folder - 
    y = 0;
    initial_simulation_titles = {...
        'Flows in selected elements - init sim', ...
        'Heads at selected nodes - init sim', ...
        'Energy cost and tariff - init sim', ...
        'Pump schedule - init sim'};
    lin_model_titles = {...
        'Flows in selected elements - MILP', ...
        'Heads at selected nodes - MILP', ...
        'Energy cost and tariff - MILP', ...
        'Pump schedule - MILP'};
    final_simulation_titles = {...
        'Flows in selected elements - final sim', ...
        'Heads at selected nodes - final sim', ...
        'Energy cost and tariff - final sim', ...
        'Pump schedule - final sim'};
    % Plot initial simulation results
    plot_2p1t_simulation_results(init_sim_output, ...
        input.init_schedule.N, input.init_schedule.S, input, sim, ...
        initial_simulation_titles, 1, folder);
    % Plot optimization results
    plot_optim_outputs(...
        input, optim_output.x, vars, optim_output, lin_model_titles, folder);
    % Plot results of simulation with optimized pump schedules
    plot_2p1t_simulation_results(optim_sim_output, ...
        optim_output.schedule.N, optim_output.schedule.S, input, ...
        sim, final_simulation_titles, 1, folder);
    y = 1;
end
